
#? Start:Stop before:Step

list = [val for val in range(11, 22)]
print("List", list)

print(list[5])

print("Start and stop", list[1:4])

print("Start:Stop Before: Step", list[1:7:2])


print("Start from end", list[-1])

print("Reverse the array", list[::-1])
print("Negative start and stop", list[:-10:-3])

